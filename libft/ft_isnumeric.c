#include "libft.h"

int8_t	ft_isnumeric(char *str)
{
	if (!str)
		return (0);
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		str++;
	return (*str ? 0 : 1);
}
